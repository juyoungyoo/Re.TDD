package calculator2;

public class Calculator {


    public int add(int num1,
                   int num2) {
        return num1 + num2;
    }

    public int minus(int num1,
                     int num2) {
        return num1-num2;
    }

    public int cross(int num1,
                     int num2) {
        return num1 * num2;
    }

    public int division(int num1,
                        int num2) {
        return num1/num2;
    }
}
